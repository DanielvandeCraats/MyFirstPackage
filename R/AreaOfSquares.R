#'
#'@title Square area
#'@description This function calculates the area of a square.
#'@param length The length of the square
#'@param width The widht of the square
#'@author Daniel van de Craats
#'@export
#'

AreaOfSquares <- function(length,width){
  area <- length*width
  return(area)
}

